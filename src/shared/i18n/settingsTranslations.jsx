// Hook pour utiliser les traductions (uniquement pour Settings)
export function useSettingsTranslation(language = 'fr') {
  const t = (key) => {
    return settingsTranslations[language]?.[key] || settingsTranslations['fr'][key] || key;
  };
  
  return { t };
}